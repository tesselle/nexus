x <- c(1, 2, 3)
y <- c(1, 2, 1)

# Closure ======================================================================
expect_identical(closure(x), c(1/6, 2/6, 3/6))
expect_identical(closure(y), c(1/4, 2/4, 1/4))

# Perturbation =================================================================
expect_identical(perturbation(x, y), c(0.125, 0.500, 0.375))

# Powering =====================================================================
expect_identical(powering(y, 2), c(1^2/4, 2^2/4, 1^2/4))
